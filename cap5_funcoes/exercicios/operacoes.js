/*

01)​ Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, 
subtração,multiplicação e divisão desses valores.

*/


const soma = function(a, b){
    return a+b 
}

const subtracao = function(a, b){
    return a-b
}

const multiplicacao = function(a, b){
    return a*b
}

const divisao = function(a, b){
    return a/b
}

console.log(soma(4,8))
console.log(subtracao(4,8))
console.log(multiplicacao(4,8))
console.log(divisao(4,8))

